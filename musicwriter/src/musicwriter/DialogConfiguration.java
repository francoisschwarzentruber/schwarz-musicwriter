    private void remplirListeEntreeMIDI()
    {
        ArrayList<String> noms = InterfaceMIDI.getMachinesEntreesMIDIDisponiblesNomsListe();     
        DefaultListModel model = new DefaultListModel();    
        for(String s : noms)   {  model.addElement(s); }
        listeEntreeMIDI.setModel(model);
        listeEntreeMIDI.setSelectedIndex(0);
    }
    
    
